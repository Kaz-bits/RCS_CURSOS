# Determinar el área de un cuarto (metros cuadrados)
longitud = float(input("Ingresa la longitud del cuarto: "))
ancho = float(input("Ingresa el ancho del cuarto: "))

# Calcular el área del cuarto
area = longitud * ancho

# Mostrar el resultado en metros cuadrados
print("El área del cuarto es: ", area, "m\u00b2")